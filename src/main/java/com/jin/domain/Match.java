package com.jin.domain;

import java.util.List;

public class Match {
	
	private Team team;
	private List<Athlete> athletes;
	
	public Team getTeam() {
		return team;
	}
	public void setTeam(Team team) {
		this.team = team;
	}
	public List<Athlete> getAthletes() {
		return athletes;
	}
	public void setAthletes(List<Athlete> athletes) {
		this.athletes = athletes;
	}

	
	
}
